    private static void printStack(Stack<String> s){
        // move this to linkedStack class
        if(s.isEmpty()){  
            System.out.println("you've nothing in your stack");
        } 
        else { 
            System.out.printf("%s TOP\n", s); 
        }
        /* Notes:
        %s means it will print a string, 
        %c single character, 
        %d integer based number, 
        %f float based number. 
        String a = "hello"; 
        Character b = 'r'; 
        Integer c = 3; 
        Double d = 3.14; 
        System.out.printf("%s\t%c\n%d\t%f\n", a, b, c, d); 
        also \t put a tabulator and \n make a new line.ï»¿
        */  
    } // end printStack
    

    
    
    
    
